    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String senhaNova = String.valueOf(varSenhaNova.getPassword());
        String senhaNovaNovamente = String.valueOf(varSenhaNovaNovamente.getPassword());
        String SenhaAtual = String.valueOf(varSenhaAtual.getPassword());
        try {
            usuarioDao = new UsuarioDaoImpl();
            sessao = HibernateUtil.abrirConexao();
            if (SenhaAtual.equals(usuario.getSenha()) && senhaNova.equals(senhaNovaNovamente)) {
                usuario.setSenha(senhaNova);
                System.out.println(usuario.getId());
                usuarioDao.salvarOuAlterar(usuario, sessao);
                JOptionPane.showMessageDialog(null, "Senha alterada com sucesso!");
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Senha Atual ou Nova nÃ£o confere, favor corrigir!");
            }
        } catch (Exception e) {
            System.out.println("Erro ao salvar senha " + e.getMessage());
        } finally {
            sessao.close();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

